package assignment;

public class Number_for {
	static int n=5;
	static void  number() {
		for(int i=1;i<=n;++i) {
			System.out.println(i);
		}
	}

	public static void main(String[] args) {
		 System.out.println("Program Starts");
		 System.out.println("Number from 1-5");
         
		number();
		System.out.println("Program Ends");
        
	}

}